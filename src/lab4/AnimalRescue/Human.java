package lab4.AnimalRescue;

public class Human {

    private String name;
    private int age;
    private int money;

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }


    public void setMoney(int money) {
        this.money = money;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }


    public int getMoney() {
        return money;
    }
}
